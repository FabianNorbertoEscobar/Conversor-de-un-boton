    private void convertirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertirButtonActionPerformed
        // TODO add your handling code here:
        bandera=1;
        String centimetrosString=centimetrosTextField.getText();
        if(!centimetrosString.isEmpty())
            centimetrosTextFieldFocusGained(null);
        else
        {
            String pulgadasString;
            pulgadasString = pulgadasTextField.getText();
            if(!pulgadasString.isEmpty())
                pulgadasTextFieldFocusGained(null);
            else
            {
                JOptionPane.showMessageDialog(this,"\n Ambas celdas están vacías \n No hay nada que convertir","ADVERTENCIA",JOptionPane.WARNING_MESSAGE);
                bandera=0;
            } 
        }
            
    }//GEN-LAST:event_convertirButtonActionPerformed

    private void centimetrosTextFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_centimetrosTextFieldFocusGained
        // TODO add your handling code here:
        pulgadasTextField.setText("");
        if(bandera==1)
        {
            String centimetrosString=centimetrosTextField.getText();
            double centimetros=Double.valueOf(centimetrosString);
            double pulgadas=centimetros/2.54;
            String pulgadasString=String.format("%.2f",pulgadas);
            pulgadasString=pulgadasString.replace(',','.');
            pulgadasTextField.setText(pulgadasString);
            bandera=0;
        }
    }//GEN-LAST:event_centimetrosTextFieldFocusGained


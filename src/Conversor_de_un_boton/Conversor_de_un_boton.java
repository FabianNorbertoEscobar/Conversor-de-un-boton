    private void centimetrosTextFieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_centimetrosTextFieldKeyTyped
        // TODO add your handling code here:
        char c=evt.getKeyChar();
        if(c=='.')
        {
        String aux;
        aux=centimetrosTextField.getText();
        for(int i=0;i<aux.length();i++)
            if(aux.charAt(i)=='.')
                evt.consume();
        }
        if((c<'0'||c>'9')&&c!='.')
            evt.consume();
    }//GEN-LAST:event_centimetrosTextFieldKeyTyped

